    public void NacrtajString(){
        Font font = new Font("Courier", Font.BOLD,24);  
        String Recenica=jTextField3.getText();    
        int DuzinaRecenice= Recenica.length();
        String ObrnutaRecenica="";      
        for(int i=DuzinaRecenice-1; i>=0;i--){
        ObrnutaRecenica=ObrnutaRecenica+Recenica.charAt(i);          
        } 
        Graphics2D ploca= (Graphics2D)jPanel1.getGraphics();           
        ploca.setFont(font);
        ploca.setColor(Color.RED);  
        ploca.drawString(ObrnutaRecenica, 350, 100);          
    }
  
    
